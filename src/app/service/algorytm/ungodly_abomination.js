 async function calculateDeliveries(
    data, 
    date_start, 
    date_end, 
    uLG95, uLG95_max, 
    dK, dK_max, 
    uLTSU, uLTSU_max, 
    uLTDK, uLTDK_max, 
    k, k2
) {
    date_start=parseDate(date_start)
    console.log(date_start + "-");
    date_end=parseDate(date_end);
    console.log(date_end + "-");

    let deliveryResults = [];
    let averageData = data;
    let day_start = parseDateStringToDayOfYear(date_start)

    let day_end = day_start+Math.round((date_end - date_start) / (1000 * 60 * 60 * 24));


    for (let x = day_start; x <= day_end; x++) {
        const row = averageData[x%365]

        const uLG95_diff = parseFloat(row[1]);
        const dK_diff = parseFloat(row[2]);
        const uLTSU_diff = parseFloat(row[3]);
        const uLTDK_diff = parseFloat(row[4]);

        let delivery_table = [0, 0, 0, 0];

        for (let a = 0; a < 2; a++) {
            if (uLG95 < uLG95_max * k) {
                delivery_table[0] = uLG95_max * k2 - uLG95;
            }
            if (dK < dK_max * k) {
                delivery_table[1] = dK_max * k2 - dK;
            }
            if (uLTSU < uLTSU_max * k) {
                delivery_table[2] = uLTSU_max * k2 - uLTSU;
            }
            if (uLTDK < uLTDK_max * k) {
                delivery_table[3] = uLTDK_max * k2 - uLTDK;
            }

            function assembleDeliveryObject(startingDay, days_passed, deliveries){
                const date = structuredClone(startingDay);;

                // Add the days
                date.setDate(date.getDate() + days_passed);

  

                // Format the new date back to dd.mm.yyyy
                let newDay = String(date.getDate()).padStart(2, '0');
                let newMonth = String(date.getMonth() + 1).padStart(2, '0'); // Months are 0-based, so add 1
                let newYear = date.getFullYear();
                
                // debugger

                return {
                    'date': `${newDay}.${newMonth}.${newYear}`,
                    "ULG95": deliveries[0],
                    "DK": deliveries[1],
                    "ULTSU": deliveries[2],
                    "ULTDK": deliveries[3]
                };
            }
            
            // debugger
            if (delivery_table.reduce((a, b) => a + b, 0) <= 40000) {
                if (!delivery_table.every(item => item == 0)) {
                    // debugger
                    delivery_table = delivery_table.map(Math.floor)
                    deliveryResults.push(assembleDeliveryObject(date_start, x-day_start, delivery_table));
                }
            } else {
                const totalDiff = uLG95_diff + dK_diff + uLTSU_diff + uLTDK_diff;
                delivery_table[0] = Math.floor((uLG95_diff / totalDiff) * 40000);
                delivery_table[1] = Math.floor((dK_diff / totalDiff) * 40000);
                delivery_table[2] = Math.floor((uLTSU_diff / totalDiff) * 40000);
                delivery_table[3] = Math.floor((uLTDK_diff / totalDiff) * 40000);
                deliveryResults.push(assembleDeliveryObject(date_start, x-day_start, delivery_table));
            }

            uLG95 += delivery_table[0];
            dK += delivery_table[1];
            uLTSU += delivery_table[2];
            uLTDK += delivery_table[3];
            delivery_table = [0, 0, 0, 0];
        }

        uLG95 = JSON.parse(JSON.stringify(uLG95-uLG95_diff));
        dK = JSON.parse(JSON.stringify(dK-dK_diff));
        uLTSU -= JSON.parse(JSON.stringify(uLTSU-uLTSU_diff));
        uLTDK -= JSON.parse(JSON.stringify(uLTDK-uLTDK_diff));
    }
    return deliveryResults;
}
